function [z]=f5(x,y)
  u_1=(10^(-3)*x.^2+y.^2)*pi*cos(pi*x).*sin(pi*y) ...
  +pi*sin(pi*x)*cos(pi*y).*(10^(-3)-1).*x.*y;
  u_2=(10^(-3)-1)*x.*y*pi*cos(pi*x).*sin(pi*y) ...
  +pi*sin(pi*x)*cos(pi*y).*(x.^2+10^(-3)*y.^2);
  
  u_1p=10^(-3)*2*x.*pi*cos(pi*x).*sin(pi*y) ...
  -(10^(-3)*x.^2+y.^2)*pi*pi.*sin(pi*x).*sin(pi*y) ...
  +pi*sin(pi*x)*cos(pi*y).*(10^(-3)-1).*y ...
  +pi*pi*cos(pi*x)*cos(pi*y).*(10^(-3)-1).*x.*y;
  
  u_2p=(10^(-3)-1)*x.*pi*cos(pi*x).*sin(pi*y) ...
  +(10^(-3)-1)*x.*y.*pi*pi*cos(pi*x).*cos(pi*y) ...
  -pi*pi*sin(pi*x)*sin(pi*y).*(x.^2+10^(-3)*y.^2) ...
  +pi*sin(pi*x)*cos(pi*y).*10^(-3)*2.*y;
  
  z=-u_1p./(x.^2+y.^2+10^(-16))+2*u_1.*x./(x.^2+y.^2+10^(-16)).^2 ...
   -u_2p./(x.^2+y.^2+10^(-16))+2*u_2.*y./(x.^2+y.^2+10^(-16)).^2;
end